public class DataPoint {

    private int year;
    private int data;

    public DataPoint(int year, int data) {
        this.year = year;
        this.data = data;
    }

    public int getYear() {
        return this.year;
    }
    public int getData() {
        return this.data;
    }

    public void setYear(int year) {
        this.year = year;
    }
    public void setData(int data) {
        this.data = data;
    }

}
